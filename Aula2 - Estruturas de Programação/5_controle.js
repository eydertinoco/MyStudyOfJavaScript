// switch/case

let nome = "Eyder";

switch(nome) {
    case "João":
        console.log(`O nome é ${nome} é um usuário`);
        break;
    case "Eyder":
        console.log(`O nome é ${nome} é um Administrador`);
        break;
    default:
        console.log(`Não não encontrado`);
        break;
}